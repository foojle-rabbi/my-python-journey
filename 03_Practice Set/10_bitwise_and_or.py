# this ia problem to print the and and or of two numbers
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

bitwise_and = num1 & num2
bitwise_or = num1 | num2

print(f"ans of bitwise and of {num1}, {num2} is = {bitwise_and}")
print(f"ans of bitwise or of {num1}, {num2} is = {bitwise_or}")
